<?php
class adding
{
 function add($x, $y) 
{
  $addition = $x + $y;
  return $addition; 
}
}
class subtraction
{

 function sub($x, $y)
{
  $sub = $x - $y;
  return $sub; 
}
}
class multiplication
{
 function mul($x, $y) 
{
  $mul = $x * $y;
  return $mul; 
}
}
class division
{
 function div($x, $y) 
{
  $div = $x / $y;
  return $div; 
}}
?>


